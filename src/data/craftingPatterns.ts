// ---------------------------------------------------------------------------
// Lookup helpers
// ---------------------------------------------------------------------------

const patternById = new Map<string, CraftingPatternDef>(
  PATTERN_DEFS.map((p) => [p.id, p]),
);

/** Return a single pattern definition by its id, or undefined if not found. */
export function getPatternDef(id: string): CraftingPatternDef | undefined {
  return patternById.get(id);
}

/** Return all patterns that belong to the given band. */
export function getPatternsForBand(band: number): CraftingPatternDef[] {
  return PATTERN_DEFS.filter((p) => p.band === band);
}

/**
 * Pick a random pattern matching the given band and source.
 * Optionally filter by a set of professions (e.g. the player's unlocked ones).
 * Returns undefined if no patterns match the criteria.
 */
export function rollPatternDrop(
  band: number,
  source: PatternSource,
  professions?: CraftingProfession[],
): CraftingPatternDef | undefined {
  let pool = PATTERN_DEFS.filter(
    (p) => p.band === band && p.source === source,
  );

  if (professions && professions.length > 0) {
    pool = pool.filter((p) => professions.includes(p.profession));
  }

  if (pool.length === 0) return undefined;

  return pool[Math.floor(Math.random() * pool.length)];
}
